const fs = require('fs');
const path = require('path');

// ─── CATEGORY CONFIGURATION ───
// Maps folder name → display label and category value
const categoryConfig = {
    'tv-units': { label: 'TV Unit Design', category: 'tv-units' },
    'bedrooms': { label: 'Bedroom Design', category: 'bedrooms' },
    'ceilings': { label: 'Ceiling Design', category: 'ceilings' },
    'wardrobes': { label: 'Wardrobe Design', category: 'wardrobes' },
    'modular-kitchen': { label: 'Modular Kitchen', category: 'modular-kitchen' },
    'walls': { label: 'Wall Design', category: 'walls' },
    'partitions': { label: 'Partition Design', category: 'partitions' },
    'temples': { label: 'Home Temple', category: 'temples' },
    'stairs': { label: 'Staircase Design', category: 'stairs' },
    'gates': { label: 'Gate Design', category: 'gates' },
    'doors': { label: 'Door Design', category: 'doors' },
    'dressing-tables': { label: 'Dressing Table', category: 'dressing-tables' },
    'shelves': { label: 'Shelf Design', category: 'shelves' },
};

// ─── VALID IMAGE EXTENSIONS ───
const validExtensions = ['.jpg', '.jpeg', '.png', '.webp', '.avif'];

// ─── IMAGES BASE FOLDER ───
const imagesFolder = path.join(__dirname, 'public', 'images');

// ─── OUTPUT FILE ───
const outputFile = path.join(__dirname, 'src', 'data', 'portfolioData.js');

let allItems = [];
let globalId = 1;
let totalImages = 0;
let skippedFolders = [];

console.log('\n📂 Scanning image folders...\n');

// ─── SCAN EACH CATEGORY FOLDER ───
Object.entries(categoryConfig).forEach(([folderName, config]) => {
    const folderPath = path.join(imagesFolder, folderName);

    // Check if folder exists
    if (!fs.existsSync(folderPath)) {
        skippedFolders.push(folderName);
        console.log(`⚠️  Folder not found — skipping: ${folderName}`);
        return;
    }

    // Read all files in folder
    let files;
    try {
        files = fs.readdirSync(folderPath);
    } catch (err) {
        console.log(`❌ Could not read folder: ${folderName}`);
        return;
    }

    // Filter to only valid image files
    const imageFiles = files.filter(file => {
        const ext = path.extname(file).toLowerCase();
        return validExtensions.includes(ext);
    });

    if (imageFiles.length === 0) {
        console.log(`📭 Empty folder — no images yet: ${folderName}`);
        return;
    }

    console.log(`✅ ${folderName}: ${imageFiles.length} images found`);
    totalImages += imageFiles.length;

    // Sort files naturally (img1, img2, img10 not img1, img10, img2)
    imageFiles.sort((a, b) => {
        return a.localeCompare(b, undefined, {
            numeric: true,
            sensitivity: 'base'
        });
    });

    // Create portfolio entry for each image
    imageFiles.forEach((file, index) => {
        const imagePath = `/images/${folderName}/${file}`;
        const itemNumber = index + 1;

        allItems.push({
            id: globalId++,
            title: `${config.label} ${itemNumber}`,
            category: config.category,
            image: imagePath,
            location: 'Hyderabad',
            isClientImage: true
        });
    });
});

// ─── GENERATE OUTPUT ───
const outputContent = `// ─────────────────────────────────────────
// portfolioData.js
// AUTO-GENERATED by generate-portfolio.js
// Generated on: ${new Date().toLocaleString('en-IN', { timeZone: 'Asia/Kolkata' })}
// Total images: ${totalImages}
// ─────────────────────────────────────────

const portfolioData = ${JSON.stringify(allItems, null, 2)};

export default portfolioData;
`;

// ─── MAKE SURE OUTPUT DIRECTORY EXISTS ───
const outputDir = path.dirname(outputFile);
if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
}

// ─── WRITE THE FILE ───
try {
    fs.writeFileSync(outputFile, outputContent, 'utf8');

    console.log('\n─────────────────────────────────────');
    console.log('✅ portfolioData.js generated successfully!');
    console.log(`📸 Total images added: ${totalImages}`);
    console.log(`📁 Output: src/data/portfolioData.js`);

    if (skippedFolders.length > 0) {
        console.log(`⚠️  Skipped folders (not found): ${skippedFolders.join(', ')}`);
    }

    console.log('─────────────────────────────────────\n');

} catch (err) {
    console.error('❌ Error writing file:', err.message);
}